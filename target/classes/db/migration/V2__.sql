-- the second script that will be run by Ninja's migration engine script
create table Student (
    rollNo integer generated by default as identity,
    name varchar(255) not null,
    DOB varchar(20) not null,
    email varchar(255) not null unique,
    mobile varchar(10) not null,
    address varchar(550) not null,
    tAttendance integer default 0,
    primary key (rollNo)
);

create table Attendance(
    id integer,
    rollNo integer not null,
    datePresent varchar(20) not null,
    primary key(id),
    foreign key(rollNo) references student(rollNo)
);